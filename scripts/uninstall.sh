#!/usr/bin/env bash
# ??????????????????????????????????????????????????????????????????????????????
# MUIOGO Development Environment Uninstall (macOS / Linux)
#
# Usage:
#   ./scripts/uninstall.sh                  # full uninstall
#   ./scripts/uninstall.sh --uninstall-solvers # also remove system solvers (brew/apt)
#   ./scripts/uninstall.sh --yes            # skip confirmation
# ??????????????????????????????????????????????????????????????????????????????
set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
PROJECT_ROOT="$(cd "$SCRIPT_DIR/.." && pwd)"

if [ -n "${CONDA_DEFAULT_ENV:-}" ]; then
    echo "ERROR: Conda environment '${CONDA_DEFAULT_ENV}' is active."
    echo "Run 'conda deactivate' and try again."
    exit 1
fi

PYTHON="python3.11"
if ! command -v "$PYTHON" &>/dev/null; then
    # Fallback to python3 if 3.11 isn't strictly available to at least run uninstall
    if command -v "python3" &>/dev/null; then
        PYTHON="python3"
    else
        echo "ERROR: python3 was not found in PATH to run uninstaller."
        exit 1
    fi
fi

exec "$PYTHON" "$SCRIPT_DIR/uninstall_dev.py" "$@"
